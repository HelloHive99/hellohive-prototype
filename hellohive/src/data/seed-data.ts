export type UserRole =
  | 'Team-Admin'
  | 'Team-OpsCoordinator'
  | 'Team-Viewer'
  | 'Vendor-Admin'
  | 'Vendor-Tech';

export interface User {
  id: string;
  name: string;
  role: UserRole;
  email: string;
  organization: string;
  associatedVendorIds?: string[]; // Links vendor-role users to vendor IDs
  companyId?: string; // For Vendor-* role users
}

export interface Property {
  id: string;
  name: string;
  address: string;
  spaces: Space[];
}

export interface Space {
  id: string;
  propertyId: string;
  name: string;
  type: string;
  assets: Asset[];
}

export type AssetHealthScore = 'good' | 'warning' | 'critical';
export type AssetCriticality = 'critical' | 'important' | 'non-critical';
export type OperationalImpact = 'high' | 'medium' | 'low';
export type TrendDirection = 'improving' | 'stable' | 'degrading';

export interface Asset {
  id: string;
  spaceId: string;
  name: string;
  type: string;
  model?: string;

  // Operational fields
  health: AssetHealthScore;
  criticality: AssetCriticality;
  operationalImpact: OperationalImpact;
  owner: string;
  lastPmDate?: string;
  nextPmDate?: string;
  pmFrequencyDays: number;

  // Manufacturer & Installation
  manufacturer?: string;
  serialNumber?: string;
  installationDate?: string;
  warrantyExpiration?: string;

  // Performance & Reliability
  pmCompletionRate?: number; // 0-100%
  lastRepairCost?: number;
  maintenanceCostYTD?: number;
  failureFrequency?: number; // failures per year
  trendDirection?: TrendDirection;

  // Ownership & Accountability
  primaryTechnicianId?: string; // links to Technician.id
  escalationOwnerId?: string; // User.id for escalation
  preferredVendorId?: string; // Vendor.id for routine maintenance

  // Parts & Dependencies
  commonParts?: string[]; // array of replacement parts
  dependencyNotes?: string;

  // Retirement tracking
  isRetired?: boolean;
  retiredAt?: string; // ISO timestamp
  retiredBy?: string; // userId
  retireReason?: string; // optional reason
}

export type VendorCategory = 'MEP' | 'Electrical' | 'Janitorial' | 'Security' | 'AV/Broadcast';

// NEW: Vendor & Technician types
export type ServiceRegion = 'Philadelphia Metro' | 'Clearwater/Tampa Bay' | 'Northeast Corridor' | 'National';
export type VendorPriorityTier = 'platinum' | 'gold' | 'standard';
export type VendorStatus = 'available' | 'limited' | 'unavailable';
export type TechnicianStatus = 'available' | 'on-job' | 'off-shift' | 'unavailable';
export type ClearanceLevel = 'none' | 'basic' | 'high-security' | 'broadcast-ops';
export type ContractType = 'MSA' | 'SOW' | 'T&M';

