export type WorkOrderStatus = 'open' | 'in-progress' | 'completed' | 'overdue' | 'dispatched';
export type WorkOrderPriority = 'low' | 'medium' | 'high' | 'urgent';

export interface WorkOrder {
  id: string;
  title: string;
  description: string;
  propertyId: string;
  spaceId: string;
  assetId?: string;
  status: WorkOrderStatus;
  priority: WorkOrderPriority;
  category: VendorCategory;
  assignedVendorId?: string;
  createdBy: string;
  createdAt: string;
  updatedAt: string;
  completedAt?: string;
  dueDate?: string;  // ISO timestamp for deadline
  cost?: number;
  assignedTechnicianId?: string;  // Now references Technician.id
  vendorReportedEta?: string;     // ISO timestamp
  dispatchedAt?: string;          // ISO — set when status transitions to 'dispatched'
  startedAt?: string;             // ISO — set when status transitions to 'in-progress'
}

export interface ActivityFeedItem {
  id: string;
  workOrderId: string;
  message: string;
  timestamp: string;
  userId: string;
}

export interface Message {
  id: string;
  conversationId: string;
  senderId: string;       // User.id or Technician.id
  senderName: string;
  senderType: 'team' | 'vendor-admin' | 'technician';
  body: string;
  timestamp: string;      // ISO
  readBy: string[];       // participant IDs who have read this message
}

export interface Conversation {
  id: string;
  type: 'work-order' | 'direct';
  workOrderId?: string;         // set when type === 'work-order'
  participantIds: string[];
  participantNames: string[];
  lastMessageAt?: string;
  lastMessagePreview?: string;
  messages: Message[];
}

